#include <iostream>


// 화면에 그림을 그릴때 사용하는 클래스
class Graphics
{
	// 수백가지의 그리는 함수를 제공합니다.
};

int main()
{
	Graphics g;
	g.DrawLine(0, 0, 100, 100);

	// 선의 색상이나 두께 등을 변경하고 싶습니다.

	// 방법 1. 함수의 인자로 전달 - windows gdi+
	g.DrawLine(0, 0, 100, 100, red, 10);
	g.DrawLine(0, 0, 100, 100, blue, 20);

	// 방법 2. Graphics 객체의 속성의 지정하도록 설계 하자 - IOS
	g.SetStrokeColor(red);
	g.SetStrokeLineWidth(20);

	g.DrawLine(0, 0, 100, 100);
	g.DrawLine(0, 0, 100, 100);


}









